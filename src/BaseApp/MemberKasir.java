    private void cariMember() {
        String keyword = txt_cari.getText();
        DefaultTableModel model = (DefaultTableModel) tb_member.getModel();
        model.setRowCount(0); // Bersihkan isi tabel sebelum menambah data baru

        try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/dinda_salon", "root", "")) {
            String sql = "SELECT * FROM member WHERE nama_member LIKE ?";
            PreparedStatement stm = conn.prepareStatement(sql);
            stm.setString(1, "%" + keyword + "%");
            ResultSet rs = stm.executeQuery();

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("id_member"),
                    rs.getString("nama_member"),
                    rs.getString("jenis_kelamin"),
                    rs.getString("notelp_member"),
                    rs.getString("alamat_member"),
                    rs.getString("rfID")
                });
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Gagal mencari data: " + ex.getMessage());
        }
    }
    
